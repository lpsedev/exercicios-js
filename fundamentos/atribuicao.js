const a = 7 // = Sinal de atribuição
let b = 5 // = Sinal de atribuição

b += a // Atribuição de ADIÇÃO --> B = B + A
b -= 4 // Subtraindo o valor de b --> b = b - 4
b *= 2 // Multiplicando o valor de b --> b = b * 2
b /= 2 // Dividindo -->  b = b / 2
b %= 2 // Modulando(B = o resto) --> b = b % 2 
