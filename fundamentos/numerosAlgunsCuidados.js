//Alguns cuidados com números.

console.log(7 / 0) // Retorna numero INFINITY.
console.log('10' / 2) // Ler a string como inteiro e divide por 2.
console.log('3' + 2)// Ele não soma os valores ele concatena.
console.log('Show!' * 2) //Reconhece que não é um número.(retorna NaN - Not a Number).
console.log(0.1 + 0.7) //Retorna um número finito de zeros.
console.log((10.345).toFixed(2))//Ele retorna 2 casas decimais.
console.log(typeof(10).toString())//Retorna uma string.

